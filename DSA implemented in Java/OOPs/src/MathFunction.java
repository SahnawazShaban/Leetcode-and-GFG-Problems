public class MathFunction {
    public static void main(String[] args) {
        System.out.println("Ceil of 3.9 is "+Math.ceil(3.9));
        System.out.println("Floor of 3.9 is "+Math.floor(3.9));
        System.out.println(Math.pow(3,3));
        System.out.println(Math.min(3.9,4.5));
        System.out.println(Math.max(3.9,4.5));
        System.out.println(Math.sqrt(3.9));
    }
}
